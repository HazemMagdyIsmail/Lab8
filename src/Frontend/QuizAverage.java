    public QuizAverage(ArrayList<Double> averages, ArrayList<String> lessonTitles) {
    this.averages = averages;
    this.lessonTitles = lessonTitles;

    setTitle("Quiz Average per Lesson");
    setSize(800, 600);
    setLocationRelativeTo(null);
    setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

    // Use BorderLayout instead of GroupLayout
    setLayout(new BorderLayout());

    // Add the chart panel
    add(createChartPanel(), BorderLayout.CENTER);
}

    private JPanel createChartPanel() {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        for (int i = 0; i < averages.size(); i++) {
            dataset.addValue(averages.get(i), "Average Score", lessonTitles.get(i));
        }

        JFreeChart chart = ChartFactory.createBarChart(
                "Quiz Average per Lesson",
                "Lesson",
                "Average Score",
                dataset,
                PlotOrientation.VERTICAL,
                false, true, false
        );

        return new ChartPanel(chart);
    }


